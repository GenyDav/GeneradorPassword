    private void verificarCheckBoxes(javax.swing.JCheckBox checkBox){
        if(!checkBox.isSelected()){
            if(numPropSel>1){
                numPropSel--;
                actualizarClave();
            }else{
                System.out.println("Sel");
                checkBox.setSelected(true);
            }
        }else{
            numPropSel++;
            actualizarClave();
        }
    }
    
